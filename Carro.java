public class Carro {

    private String carroNome;
    private String carroPlaca;

    public Carro(String carroNome, String carroPlaca) {
        this.carroNome = carroNome;
        this.carroPlaca = carroPlaca;
    }

    public String getCarroNome() {
        return carroNome;
    }

    public void setCarroNome(String carroNome) {
        this.carroNome = carroNome;
    }

    public String getCarroPlaca() {
        return carroPlaca;
    }

    public void setCarroPlaca(String carroPlaca) {
        this.carroPlaca = carroPlaca;
    }


}


